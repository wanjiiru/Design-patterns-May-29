
<?php
class Book
{
    private $title;
    private $author;
    private $content;
    public function __construct($title, $author, $content)
    {
        $this->title;
        $this->author;
        $this->content;
    }
    public function getTitle()
    {
        return $this->title;
    }
    public function getAuthor()
    {
        return $this->author;
    }
    public function getContent()
    {
        return $this->content;
    }


 ?>
